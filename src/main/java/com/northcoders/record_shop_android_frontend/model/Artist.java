package com.northcoders.record_shop_android_frontend.model;

public class Artist {
    long id;
    String name;

    public Artist() {}

    public Artist(long id, String name) {
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
